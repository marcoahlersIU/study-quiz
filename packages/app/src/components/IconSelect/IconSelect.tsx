export type SelectableIcon = keyof typeof selectableIcons;

interface IconSelectProps {
  selectedIcon: SelectableIcon;
  onSelect: (icon: SelectableIcon) => void;
}

const IconSelect: React.FC<IconSelectProps> = ({ selectedIcon, onSelect }) => {
  const IconComponent = selectableIcons[selectedIcon];

  return (
    <Popover>
      <PopoverTrigger asChild>
        <Button size="sm" variant="outline" className="h-10">
          <IconComponent className="w-4 h-4 stroke-[1.75px]" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-auto p-2" align="center" side="left">
        <div className="grid grid-cols-12">
          {Object.keys(selectableIcons).map((icon) => (
            <Button
              size="icon"
              variant="ghost"
              key={icon}
              onClick={() => onSelect(icon as SelectableIcon)}
              className="h-8 w-8"
            >
              {React.createElement(selectableIcons[icon as SelectableIcon], {
                className: 'w-4 h-4 stroke-[1.75px]',
              })}
            </Button>
          ))}
        </div>
      </PopoverContent>
    </Popover>
  );
};

export default IconSelect;
